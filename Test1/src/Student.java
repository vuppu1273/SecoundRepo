
public class Student {

	
	private int Id ;
	private String name ;
	private static String School;
	
	
	
	public static String getSchool() {
		return School;
	}
	public static void setSchool(String school) {
		School = school;
	}
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
